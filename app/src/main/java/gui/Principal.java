	private int checkAndGetAño(String año) throws Exception {
		int year = Integer.parseInt(año);
		if (year < 0 || year > new java.sql.Date(System.currentTimeMillis()).getYear()) {
			throw new Exception("Invalid year");
		}
		return year;
	}
	
